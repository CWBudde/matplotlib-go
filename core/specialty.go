// Eventplot adds a collection of short event markers to the axes.
func (a *Axes) Eventplot(positions [][]float64, opts ...EventPlotOptions) *EventCollection {
	if a == nil || len(positions) == 0 {
		return nil
	}
	cfg := EventPlotOptions{
		Orientation: EventOrientationVertical,
		LineWidth:   1.5,
		Alpha:       1,
	}
	if len(opts) > 0 {
		cfg = opts[0]
		if cfg.LineWidth <= 0 {
			cfg.LineWidth = 1.5
		}
		if cfg.Alpha <= 0 {
			cfg.Alpha = 1
		}
	}

	offsets := cfg.LineOffsets
	if len(offsets) == 0 {
		offsets = make([]float64, len(positions))
		for i := range offsets {
			offsets[i] = float64(i + 1)
		}
	}
	lengths := cfg.LineLengths
	if len(lengths) == 0 {
		lengths = make([]float64, len(positions))
		for i := range lengths {
			lengths[i] = 0.8
		}
	}

	segments := make([][]geom.Pt, 0)
	colors := make([]render.Color, 0)
	for i, group := range positions {
		offset := floatAt(offsets, i, float64(i+1))
		length := math.Abs(floatAt(lengths, i, 0.8))
		half := length / 2
		col := colorAt(a.NextColor(), cfg.Colors, i)
		col.A *= clampOneToOne(cfg.Alpha)
		for _, value := range group {
			if !isFinite(value) {
				continue
			}
			segment := []geom.Pt{
				{X: value, Y: offset - half},
				{X: value, Y: offset + half},
			}
			if cfg.Orientation == EventOrientationHorizontal {
				segment = []geom.Pt{
					{X: offset - half, Y: value},
					{X: offset + half, Y: value},
				}
			}
			segments = append(segments, segment)
			colors = append(colors, col)
		}
	}
	if len(segments) == 0 {
		return nil
	}

	collection := &EventCollection{
		LineCollection: LineCollection{
			Collection: Collection{Label: cfg.Label, Alpha: 1, z: 2},
			Segments:   segments,
			Colors:     colors,
			LineWidth:  cfg.LineWidth,
			LineCap:    render.CapButt,
			LineJoin:   render.JoinRound,
		},
		Positions:   positions,
		Orientation: cfg.Orientation,
		LineOffsets: append([]float64(nil), offsets...),
		LineLengths: append([]float64(nil), lengths...),
	}
	a.AddCollection(collection)
	return collection
}

// Hexbin bins points into a staggered hexagonal grid and colors occupied bins by aggregate value.
func (a *Axes) Hexbin(x, y []float64, opts ...HexbinOptions) *HexbinCollection {
	if a == nil || len(x) == 0 || len(y) == 0 {
		return nil
	}

	n := min(len(x), len(y))
	cfg := HexbinOptions{
		GridSizeX: 18,
		MinCount:  1,
		Colormap:  "viridis",
		Alpha:     1,
		LineWidth: 0.75,
	}
	if len(opts) > 0 {
		cfg = opts[0]
		if cfg.GridSizeX <= 0 {
			cfg.GridSizeX = 18
		}
		if cfg.MinCount <= 0 {
			cfg.MinCount = 1
		}
		if cfg.Colormap == "" {
			cfg.Colormap = "viridis"
		}
		if cfg.Alpha <= 0 {
			cfg.Alpha = 1
		}
		if cfg.LineWidth <= 0 {
			cfg.LineWidth = 0.75
		}
	}

	extent := cfg.Extent
	if extent == nil {
		rect := finitePointRect(x[:n], y[:n])
		extent = &rect
	}
	if extent.W() == 0 {
		extent.Max.X = extent.Min.X + 1
	}
	if extent.H() == 0 {
		extent.Max.Y = extent.Min.Y + 1
	}

	gridX := max(1, cfg.GridSizeX)
	gridY := cfg.GridSizeY
	if gridY <= 0 {
		gridY = max(1, int(float64(gridX)/math.Sqrt(3)))
	}

	xMin, xMax := extent.Min.X, extent.Max.X
	yMin, yMax := extent.Min.Y, extent.Max.Y
	padding := 1e-9 * (xMax - xMin)
	xMin -= padding
	xMax += padding

	xStep := (xMax - xMin) / float64(gridX)
	yStep := (yMax - yMin) / float64(gridY)
	if xStep <= 0 {
		xStep = 1
	}
	if yStep <= 0 {
		yStep = 1
	}

	type bin struct {
		center geom.Pt
		count  float64
		sum    float64
	}

	bins := map[hexbinKey]*bin{}
	for i := range n {
		xi, yi := x[i], y[i]
		if !isFinite(xi) || !isFinite(yi) {
			continue
		}
		k, center, ok := matplotlibHexbinCenter(xi, yi, xMin, yMin, gridX, gridY, xStep, yStep)
		if !ok {
			continue
		}
		entry := bins[k]
		if entry == nil {
			entry = &bin{
				center: center,
			}
			bins[k] = entry
		}
		entry.count++
		if i < len(cfg.C) && isFinite(cfg.C[i]) {
			entry.sum += cfg.C[i]
		} else {
			entry.sum += 1
		}
	}
	if len(bins) == 0 {
		return nil
	}

	keys := make([]hexbinKey, 0, len(bins))
	for k := range bins {
		keys = append(keys, k)
	}
	slices.SortFunc(keys, func(a, b hexbinKey) int {
		if a.lattice != b.lattice {
			return a.lattice - b.lattice
		}
		if a.row != b.row {
			return a.row - b.row
		}
		return a.col - b.col
	})

	polygons := make([][]geom.Pt, 0, len(keys))
	values := make([]float64, 0, len(keys))
	counts := make([]float64, 0, len(keys))
	centers := make([]geom.Pt, 0, len(keys))
	rx := xStep / 2
	ry := yStep / 3
	for _, k := range keys {
		entry := bins[k]
		if int(entry.count) < cfg.MinCount {
			continue
		}
		value := entry.count
		if len(cfg.C) > 0 {
			switch cfg.Reduce {
			case "sum":
				value = entry.sum
			case "count":
				value = entry.count
			default:
				value = entry.sum / entry.count
			}
		}
		polygons = append(polygons, specialtyHexagon(entry.center, rx, ry))
		values = append(values, value)
		counts = append(counts, entry.count)
		centers = append(centers, entry.center)
	}
	if len(polygons) == 0 {
		return nil
	}

	mapping := resolveScalarMapValues(values, cfg.Colormap, cfg.VMin, cfg.VMax)
	faceColors := make([]render.Color, len(values))
	for i, value := range values {
		faceColors[i] = mapping.Color(value, cfg.Alpha)
	}
	edgeColor := render.Color{R: 1, G: 1, B: 1, A: 0.9}
	if cfg.EdgeColor != nil {
		edgeColor = *cfg.EdgeColor
	}
	edgeColors := faceColors
	if cfg.EdgeColor != nil {
		edgeColors = nil
	}
	collection := &HexbinCollection{
		PolyCollection: PolyCollection{
			PatchCollection: PatchCollection{
				Collection: Collection{
					Label:    cfg.Label,
					Alpha:    1,
					Colormap: mapping.Colormap,
					VMin:     mapping.VMin,
					VMax:     mapping.VMax,
					z:        2,
				},
				FaceColors: faceColors,
				EdgeColor:  edgeColor,
				EdgeColors: edgeColors,
				EdgeWidth:  cfg.LineWidth,
			},
			Polygons: polygons,
		},
		BinCenters: centers,
		Values:     values,
		Counts:     counts,
	}
	a.AddCollection(collection)
	return collection
}

// Pie draws pie-slice wedges and returns the artists grouped in a container.
func (a *Axes) Pie(values []float64, opts ...PieOptions) *PieContainer {
	if a == nil || len(values) == 0 {
		return nil
	}
	cfg := PieOptions{
		Center:        geom.Pt{},
		Radius:        1,
		StartAngle:    0,
		LabelDistance: 1.15,
		PctDistance:   0.62,
		LineWidth:     1,
		Alpha:         1,
		Coords:        Coords(CoordData),
	}
	if len(opts) > 0 {
		cfg = opts[0]
		if cfg.Radius <= 0 {
			cfg.Radius = 1
		}
		if cfg.LabelDistance <= 0 {
			cfg.LabelDistance = 1.15
		}
		if cfg.PctDistance <= 0 {
			cfg.PctDistance = 0.62
		}
		if cfg.LineWidth <= 0 {
			cfg.LineWidth = 1
		}
		if cfg.Alpha <= 0 {
			cfg.Alpha = 1
		}
		if cfg.Coords == (CoordinateSpec{}) {
			cfg.Coords = Coords(CoordData)
		}
	}

	total := 0.0
	filtered := make([]float64, len(values))
	for i, value := range values {
		if value > 0 && isFinite(value) {
			filtered[i] = value
			total += value
		}
	}
	if total == 0 {
		return nil
	}

	container := &PieContainer{}
	edgeColor := render.Color{R: 1, G: 1, B: 1, A: 1}
	if cfg.EdgeColor != nil {
		edgeColor = *cfg.EdgeColor
	}

	theta := cfg.StartAngle
	counterClockwise := specialtyBool(cfg.CounterClockwise, true)
	for i, value := range filtered {
		if value <= 0 {
			continue
		}
		fraction := value / total
		span := fraction * 360
		end := theta + span
		if !counterClockwise {
			end = theta - span
		}
		mid := (theta + end) / 2
		explode := floatAt(cfg.Explode, i, 0)
		offset := geom.Pt{
			X: math.Cos(mid*math.Pi/180) * cfg.Radius * explode,
			Y: math.Sin(mid*math.Pi/180) * cfg.Radius * explode,
		}
		color := colorAt(a.NextColor(), cfg.Colors, i)
		color.A *= clampOneToOne(cfg.Alpha)
		wedge := &Wedge{
			Patch: Patch{
				FaceColor: color,
				EdgeColor: edgeColor,
				EdgeWidth: cfg.LineWidth,
				Alpha:     1,
				Label:     stringAt("", cfg.Labels, i),
				z:         2,
			},
			Center: geom.Pt{X: cfg.Center.X + offset.X, Y: cfg.Center.Y + offset.Y},
			Radius: cfg.Radius,
			Width:  cfg.Width,
			Theta1: theta,
			Theta2: end,
			Coords: cfg.Coords,
		}
		a.AddPatch(wedge)
		container.Wedges = append(container.Wedges, wedge)

		if label := stringAt("", cfg.Labels, i); label != "" {
			labelPt := piePoint(wedge.Center, cfg.Radius*cfg.LabelDistance, mid)
			clipOn := false
			container.Labels = append(container.Labels, a.Text(labelPt.X, labelPt.Y, label, TextOptions{
				Coords: cfg.Coords,
				HAlign: pieAlign(mid),
				VAlign: TextVAlignMiddle,
				ClipOn: &clipOn,
			}))
		}
		if cfg.AutoPct != "" {
			autoPt := piePoint(wedge.Center, cfg.Radius*cfg.PctDistance, mid)
			autoText := fmt.Sprintf(cfg.AutoPct, fraction*100)
			clipOn := false
			container.AutoText = append(container.AutoText, a.Text(autoPt.X, autoPt.Y, autoText, TextOptions{
				Coords: cfg.Coords,
				HAlign: TextAlignCenter,
				VAlign: TextVAlignMiddle,
				ClipOn: &clipOn,
			}))
		}

		theta = end
	}

	if cfg.Coords == Coords(CoordData) {
		padding := cfg.Radius * 1.25
		a.SetXLim(cfg.Center.X-padding, cfg.Center.X+padding)
		a.SetYLim(cfg.Center.Y-padding, cfg.Center.Y+padding)
		a.SetAxisEqual()
		if !specialtyBool(cfg.Frame, false) {
			hideAxesFrame(a)
		}
	}
	return container
}

// Violinplot draws one violin body per dataset and optional summary lines.
func (a *Axes) Violinplot(data [][]float64, opts ...ViolinOptions) *ViolinContainer {
	if a == nil || len(data) == 0 {
		return nil
	}
	cfg := ViolinOptions{
		EdgeWidth: 1,
		Alpha:     0.65,
		Points:    100,
	}
	if len(opts) > 0 {
		cfg = opts[0]
		if cfg.EdgeWidth <= 0 {
			cfg.EdgeWidth = 1
		}
		if cfg.Alpha <= 0 {
			cfg.Alpha = 0.65
		}
		if cfg.Points < 8 {
			cfg.Points = 100
		}
	}

	polygons := make([][]geom.Pt, 0, len(data))
	faceColors := make([]render.Color, 0, len(data))
	medianSegments := make([][]geom.Pt, 0, len(data))
	meanSegments := make([][]geom.Pt, 0, len(data))
	extremaSegments := make([][]geom.Pt, 0, len(data)*3)

	for i, series := range data {
		values := specialtyFiniteValues(series)
		if len(values) == 0 {
			continue
		}

		width := math.Abs(floatAt(cfg.Widths, i, 0.5))
		if width == 0 {
			width = 0.5
		}
		position := floatAt(cfg.Positions, i, float64(i+1))
		stats := specialtyViolinStats(values)
		grid, density := specialtyKDE(values, cfg.Points, cfg.Bandwidth)
		if len(grid) == 0 || len(density) == 0 {
			continue
		}

		maxDensity := 0.0
		for _, d := range density {
			if d > maxDensity {
				maxDensity = d
			}
		}
		if maxDensity == 0 {
			maxDensity = 1
		}

		left := make([]geom.Pt, 0, len(grid))
		right := make([]geom.Pt, 0, len(grid))
		for j := range grid {
			halfWidth := density[j] / maxDensity * width * 0.5
			left = append(left, geom.Pt{X: position - halfWidth, Y: grid[j]})
			right = append(right, geom.Pt{X: position + halfWidth, Y: grid[j]})
		}
		polygon := make([]geom.Pt, 0, len(left)+len(right))
		polygon = append(polygon, left...)
		for j := len(right) - 1; j >= 0; j-- {
			polygon = append(polygon, right[j])
		}
		polygons = append(polygons, polygon)

		color := colorAt(a.NextColor(), cfg.Colors, i)
		color.A *= clampOneToOne(cfg.Alpha)
		faceColors = append(faceColors, color)

		if specialtyBool(cfg.ShowMeans, false) {
			meanSegments = append(meanSegments, []geom.Pt{
				{X: position - width*0.25, Y: stats.mean},
				{X: position + width*0.25, Y: stats.mean},
			})
		}
		if specialtyBool(cfg.ShowMedians, true) {
			medianSegments = append(medianSegments, []geom.Pt{
				{X: position - width*0.3, Y: stats.median},
				{X: position + width*0.3, Y: stats.median},
			})
		}
		if specialtyBool(cfg.ShowExtrema, true) {
			extremaSegments = append(extremaSegments,
				[]geom.Pt{{X: position, Y: stats.min}, {X: position, Y: stats.max}},
				[]geom.Pt{{X: position - width*0.25, Y: stats.min}, {X: position + width*0.25, Y: stats.min}},
				[]geom.Pt{{X: position - width*0.25, Y: stats.max}, {X: position + width*0.25, Y: stats.max}},
			)
		}
	}

	if len(polygons) == 0 {
		return nil
	}

	edgeColor := render.Color{R: 0.12, G: 0.12, B: 0.12, A: 0.9}
	if cfg.EdgeColor != nil {
		edgeColor = *cfg.EdgeColor
	}
	lineColor := edgeColor
	if cfg.LineColor != nil {
		lineColor = *cfg.LineColor
	}

	container := &ViolinContainer{
		Bodies: &PolyCollection{
			PatchCollection: PatchCollection{
				Collection: Collection{Label: cfg.Label, Alpha: 1, z: 2},
				FaceColors: faceColors,
				EdgeColor:  edgeColor,
				EdgeWidth:  cfg.EdgeWidth,
			},
			Polygons: polygons,
		},
	}
	a.AddCollection(container.Bodies)

	if len(meanSegments) > 0 {
		container.Means = &LineCollection{
			Collection: Collection{Alpha: 1, z: 2.3},
			Segments:   meanSegments,
			Color:      lineColor,
			LineWidth:  math.Max(cfg.EdgeWidth, 1.25),
			LineCap:    render.CapRound,
		}
		a.AddCollection(container.Means)
	}
	if len(medianSegments) > 0 {
		container.Medians = &LineCollection{
			Collection: Collection{Alpha: 1, z: 2.35},
			Segments:   medianSegments,
			Color:      render.Color{R: 1, G: 1, B: 1, A: 0.95},
			LineWidth:  math.Max(cfg.EdgeWidth, 1.5),
			LineCap:    render.CapRound,
		}
		a.AddCollection(container.Medians)
	}
	if len(extremaSegments) > 0 {
		container.Extrema = &LineCollection{
			Collection: Collection{Alpha: 1, z: 2.1},
			Segments:   extremaSegments,
			Color:      lineColor,
			LineWidth:  math.Max(cfg.EdgeWidth*0.9, 1),
			LineCap:    render.CapRound,
		}
		a.AddCollection(container.Extrema)
	}
	return container
}

// Table adds a simple table artist positioned in axes coordinates by default.
func (a *Axes) Table(opts ...TableOptions) *Table {
	if a == nil {
		return nil
	}
	cfg := TableOptions{
		BBox:            geom.Rect{Min: geom.Pt{X: 0, Y: 0}, Max: geom.Pt{X: 1, Y: 1}},
		Coords:          Coords(CoordAxes),
		FontSize:        a.resolvedRC().FontSize,
		TextColor:       a.resolvedRC().DefaultTextColor(),
		HeaderTextColor: a.resolvedRC().DefaultTextColor(),
		HeaderFillColor: render.Color{R: 1, G: 1, B: 1, A: 1},
		CellFillColor:   render.Color{R: 1, G: 1, B: 1, A: 1},
		EdgeColor:       render.Color{R: 0, G: 0, B: 0, A: 1},
		LineWidth:       1,
		CellLoc:         "right",
		RowLoc:          "left",
		ColLoc:          "center",
	}
	if len(opts) > 0 {
		cfg = opts[0]
		if cfg.BBox == (geom.Rect{}) {
			cfg.BBox = geom.Rect{Min: geom.Pt{X: 0, Y: 0}, Max: geom.Pt{X: 1, Y: 1}}
		}
		if cfg.Coords == (CoordinateSpec{}) {
			cfg.Coords = Coords(CoordAxes)
		}
		if cfg.FontSize <= 0 {
			cfg.FontSize = a.resolvedRC().FontSize
		}
		if cfg.TextColor == (render.Color{}) {
			cfg.TextColor = a.resolvedRC().DefaultTextColor()
		}
		if cfg.HeaderTextColor == (render.Color{}) {
			cfg.HeaderTextColor = a.resolvedRC().DefaultTextColor()
		}
		if cfg.HeaderFillColor == (render.Color{}) {
			cfg.HeaderFillColor = render.Color{R: 1, G: 1, B: 1, A: 1}
		}
		if cfg.CellFillColor == (render.Color{}) {
			cfg.CellFillColor = render.Color{R: 1, G: 1, B: 1, A: 1}
		}
		if cfg.EdgeColor == (render.Color{}) {
			cfg.EdgeColor = render.Color{R: 0, G: 0, B: 0, A: 1}
		}
		if cfg.LineWidth <= 0 {
			cfg.LineWidth = 1
		}
		if cfg.CellLoc == "" {
			cfg.CellLoc = "right"
		}
		if cfg.RowLoc == "" {
			cfg.RowLoc = "left"
		}
		if cfg.ColLoc == "" {
			cfg.ColLoc = "center"
		}
	}

	rows := len(cfg.CellText)
	cols := 0
	for _, row := range cfg.CellText {
		cols = max(cols, len(row))
	}
	if rows == 0 && len(cfg.RowLabels) == 0 && len(cfg.ColLabels) == 0 {
		return nil
	}
	if cols == 0 {
		cols = 1
	}

	hasRowLabels := len(cfg.RowLabels) > 0
	hasColLabels := len(cfg.ColLabels) > 0
	gridRows := rows
	if hasColLabels {
		gridRows++
	}
	gridCols := cols + boolOffset(hasRowLabels)
	dataCellW := cfg.BBox.W() / float64(cols)
	rowLabelW := 0.0
	if hasRowLabels {
		rowLabelW = tableRowLabelWidth(cfg.RowLabels, dataCellW)
	}
	cellH := cfg.BBox.H() / float64(gridRows)
	cellAlign := tableTextAlign(cfg.CellLoc, TextAlignRight)
	rowAlign := tableTextAlign(cfg.RowLoc, TextAlignLeft)
	colAlign := tableTextAlign(cfg.ColLoc, TextAlignCenter)

	cells := make([][]tableCell, gridRows)
	for r := range gridRows {
		cells[r] = make([]tableCell, gridCols)
		for c := range gridCols {
			cells[r][c] = tableCell{
				Fill:   cfg.CellFillColor,
				Rect:   tableCellRect(cfg.BBox, gridRows, cols, hasRowLabels, hasColLabels, rowLabelW, r, c),
				HAlign: cellAlign,
			}
		}
	}
	if hasColLabels {
		for c := 0; c < cols; c++ {
			col := c + boolOffset(hasRowLabels)
			cells[0][col] = tableCell{
				Text:     stringAt("", cfg.ColLabels, c),
				Fill:     cfg.HeaderFillColor,
				IsHeader: true,
				Rect:     tableCellRect(cfg.BBox, gridRows, cols, hasRowLabels, hasColLabels, rowLabelW, 0, col),
				HAlign:   colAlign,
			}
		}
	}
	if hasRowLabels {
		for r := 0; r < rows; r++ {
			row := r + boolOffset(hasColLabels)
			cells[row][0] = tableCell{
				Text:     stringAt("", cfg.RowLabels, r),
				Fill:     cfg.HeaderFillColor,
				IsHeader: true,
				Rect: geom.Rect{
					Min: geom.Pt{X: cfg.BBox.Min.X - rowLabelW, Y: cfg.BBox.Max.Y - float64(row+1)*cellH},
					Max: geom.Pt{X: cfg.BBox.Min.X, Y: cfg.BBox.Max.Y - float64(row)*cellH},
				},
				HAlign: rowAlign,
			}
		}
	}
	for r := 0; r < rows; r++ {
		for c := 0; c < cols; c++ {
			fill := cfg.CellFillColor
			if r < len(cfg.CellColors) && c < len(cfg.CellColors[r]) && cfg.CellColors[r][c] != (render.Color{}) {
				fill = cfg.CellColors[r][c]
			}
			row := r + boolOffset(hasColLabels)
			col := c + boolOffset(hasRowLabels)
			cells[row][col] = tableCell{
				Text:   stringAt("", cfg.CellText[r], c),
				Fill:   fill,
				Rect:   tableCellRect(cfg.BBox, gridRows, cols, hasRowLabels, hasColLabels, rowLabelW, row, col),
				HAlign: cellAlign,
			}
		}
	}

	table := &Table{
		Cells:           cells,
		BBox:            cfg.BBox,
		Coords:          cfg.Coords,
		FontSize:        cfg.FontSize,
		TextColor:       cfg.TextColor,
		HeaderTextColor: cfg.HeaderTextColor,
		EdgeColor:       cfg.EdgeColor,
		LineWidth:       cfg.LineWidth,
		z:               4,
	}
	a.Add(table)
	return table
}

func tableTextAlign(loc string, fallback TextAlign) TextAlign {
	switch strings.ToLower(strings.TrimSpace(loc)) {
	case "left":
		return TextAlignLeft
	case "center", "centre":
		return TextAlignCenter
	case "right":
		return TextAlignRight
	default:
		return fallback
	}
}

func tableRowLabelWidth(labels []string, dataCellW float64) float64 {
	maxLen := 0
	for _, label := range labels {
		maxLen = max(maxLen, len([]rune(label)))
	}
	width := 0.015 + float64(maxLen)*0.025
	if width < 0.04 {
		width = 0.04
	}
	if dataCellW > 0 && width > dataCellW*0.5 {
		width = dataCellW * 0.5
	}
	return width
}

func tableCellRect(bbox geom.Rect, gridRows, dataCols int, hasRowLabels, hasColLabels bool, rowLabelW float64, row, col int) geom.Rect {
	if gridRows <= 0 || dataCols <= 0 {
		return geom.Rect{}
	}
	cellH := bbox.H() / float64(gridRows)
	y0 := bbox.Max.Y - float64(row+1)*cellH
	y1 := bbox.Max.Y - float64(row)*cellH
	if hasRowLabels && col == 0 {
		if hasColLabels && row == 0 {
			return geom.Rect{}
		}
		return geom.Rect{
			Min: geom.Pt{X: bbox.Min.X - rowLabelW, Y: y0},
			Max: geom.Pt{X: bbox.Min.X, Y: y1},
		}
	}

	dataCol := col
	if hasRowLabels {
		dataCol--
	}
	if dataCol < 0 || dataCol >= dataCols {
		return geom.Rect{}
	}
	cellW := bbox.W() / float64(dataCols)
	x0 := bbox.Min.X + float64(dataCol)*cellW
	return geom.Rect{
		Min: geom.Pt{X: x0, Y: y0},
		Max: geom.Pt{X: x0 + cellW, Y: y1},
	}
}

func tableTextAnchor(rect geom.Rect, align TextAlign) geom.Pt {
	x := rect.Min.X + rect.W()/2
	switch align {
	case TextAlignLeft:
		x = rect.Min.X + rect.W()*0.1
	case TextAlignRight:
		x = rect.Max.X - rect.W()*0.1
	}
	return geom.Pt{
		X: x,
		Y: rect.Min.Y + rect.H()/2,
	}
}

// Draw renders the wedge path using the embedded patch styling.
func (w *Wedge) Draw(ren render.Renderer, ctx *DrawContext) {
	if w == nil || ctx == nil || ren == nil || w.Radius <= 0 {
		return
	}
	path := buildDisplayPath(ctx, w.Coords, w.localPath(), geom.Identity())
	w.drawStyledPath(ren, path, geom.Path{})
}

// Bounds returns the wedge's data-space bounds when applicable.
func (w *Wedge) Bounds(*DrawContext) geom.Rect {
	if w == nil || w.Radius <= 0 || !isDataCoords(w.Coords) {
		return geom.Rect{}
	}
	bounds, _ := pathBounds(w.localPath())
	return bounds
}

func (w *Wedge) localPath() geom.Path {
	outer := specialtyArcPoints(w.Center, w.Radius, w.Theta1, w.Theta2)
	if len(outer) == 0 {
		return geom.Path{}
	}
	if w.Width <= 0 || w.Width >= w.Radius {
		points := make([]geom.Pt, 0, len(outer)+1)
		points = append(points, w.Center)
		points = append(points, outer...)
		return polygonPath(points, true)
	}
	inner := specialtyArcPoints(w.Center, w.Radius-w.Width, w.Theta2, w.Theta1)
	points := make([]geom.Pt, 0, len(outer)+len(inner))
	points = append(points, outer...)
	points = append(points, inner...)
	return polygonPath(points, true)
}

// Draw renders the table cells and text labels.
func (t *Table) Draw(r render.Renderer, ctx *DrawContext) {
	if t == nil || ctx == nil || r == nil || len(t.Cells) == 0 || len(t.Cells[0]) == 0 {
		return
	}
	textRen, ok := r.(render.TextDrawer)
	if !ok {
		return
	}

	for row := range t.Cells {
		for col := range t.Cells[row] {
			cell := t.Cells[row][col]
			if cell.Rect == (geom.Rect{}) {
				continue
			}
			path := buildDisplayPath(ctx, t.Coords, patchRectPath(cell.Rect), geom.Identity())
			r.Path(path, &render.Paint{
				Fill:      cell.Fill,
				Stroke:    t.EdgeColor,
				LineWidth: t.LineWidth,
				LineJoin:  render.JoinMiter,
				LineCap:   render.CapButt,
			})

			text := cell.Text
			if displayTextIsEmpty(text) {
				continue
			}
			anchor := transformedPoint(ctx, t.Coords, tableTextAnchor(cell.Rect, cell.HAlign), 0, 0)
			layout := measureSingleLineTextLayout(r, text, t.FontSize, ctx.RC.FontKey)
			color := t.TextColor
			if cell.IsHeader {
				color = t.HeaderTextColor
			}
			drawDisplayText(textRen, text, alignedSingleLineOrigin(anchor, layout, cell.HAlign, textLayoutVAlignCenter), t.FontSize, color, ctx.RC.FontKey)
		}
	}
}

