// EventOrientation controls whether eventplot lines are drawn vertically or horizontally.
type EventOrientation uint8

const (
	EventOrientationVertical EventOrientation = iota
	EventOrientationHorizontal
)

// EventPlotOptions configures Axes.Eventplot.
type EventPlotOptions struct {
	Orientation EventOrientation
	LineOffsets []float64
	LineLengths []float64
	Colors      []render.Color
	LineWidth   float64
	Alpha       float64
	Label       string
}

// EventCollection is a thin LineCollection wrapper used for eventplot.
type EventCollection struct {
	LineCollection
	Positions   [][]float64
	Orientation EventOrientation
	LineOffsets []float64
	LineLengths []float64
}

// HexbinOptions configures Axes.Hexbin.
type HexbinOptions struct {
	GridSizeX int
	GridSizeY int
	Extent    *geom.Rect
	C         []float64
	Reduce    string
	MinCount  int
	Colormap  string
	VMin      *float64
	VMax      *float64
	Alpha     float64
	EdgeColor *render.Color
	LineWidth float64
	Label     string
}

// HexbinCollection is a scalar-mappable polygon collection generated by hexbin.
type HexbinCollection struct {
	PolyCollection
	BinCenters []geom.Pt
	Values     []float64
	Counts     []float64
}

