type specialtyViolinSummary struct {
	min    float64
	max    float64
	mean   float64
	median float64
}

func specialtyArcPoints(center geom.Pt, radius, theta1, theta2 float64) []geom.Pt {
	if radius <= 0 {
		return nil
	}
	span := theta2 - theta1
	steps := max(12, int(math.Ceil(math.Abs(span)/12))+1)
	points := make([]geom.Pt, 0, steps)
	for i := range steps {
		t := float64(i) / float64(max(steps-1, 1))
		angle := (theta1 + span*t) * math.Pi / 180
		points = append(points, geom.Pt{
			X: center.X + radius*math.Cos(angle),
			Y: center.Y + radius*math.Sin(angle),
		})
	}
	return points
}

func specialtyHexagon(center geom.Pt, rx, ry float64) []geom.Pt {
	return []geom.Pt{
		{X: center.X - rx*0.5, Y: center.Y - ry},
		{X: center.X + rx*0.5, Y: center.Y - ry},
		{X: center.X + rx, Y: center.Y},
		{X: center.X + rx*0.5, Y: center.Y + ry},
		{X: center.X - rx*0.5, Y: center.Y + ry},
		{X: center.X - rx, Y: center.Y},
	}
}

func specialtyFiniteValues(values []float64) []float64 {
	out := make([]float64, 0, len(values))
	for _, value := range values {
		if isFinite(value) {
			out = append(out, value)
		}
	}
	slices.Sort(out)
	return out
}

func specialtyViolinStats(values []float64) specialtyViolinSummary {
	sum := 0.0
	for _, value := range values {
		sum += value
	}
	return specialtyViolinSummary{
		min:    values[0],
		max:    values[len(values)-1],
		mean:   sum / float64(len(values)),
		median: percentileSorted(values, 50),
	}
}

func specialtyKDE(values []float64, points int, bandwidth float64) ([]float64, []float64) {
	if len(values) == 0 {
		return nil, nil
	}
	minValue := values[0]
	maxValue := values[len(values)-1]
	if minValue == maxValue {
		minValue -= 0.5
		maxValue += 0.5
	}
	if bandwidth <= 0 {
		mean := 0.0
		for _, value := range values {
			mean += value
		}
		mean /= float64(len(values))
		variance := 0.0
		for _, value := range values {
			diff := value - mean
			variance += diff * diff
		}
		std := math.Sqrt(variance / float64(len(values)))
		bandwidth = 1.06 * std * math.Pow(float64(len(values)), -0.2)
		if bandwidth <= 0 || !isFinite(bandwidth) {
			bandwidth = (maxValue - minValue) / 12
		}
		if bandwidth <= 0 {
			bandwidth = 1
		}
	}

	grid := make([]float64, points)
	density := make([]float64, points)
	span := maxValue - minValue
	for i := range points {
		t := float64(i) / float64(max(points-1, 1))
		x := minValue + span*t
		grid[i] = x
		sum := 0.0
		for _, value := range values {
			z := (x - value) / bandwidth
			sum += math.Exp(-0.5 * z * z)
		}
		density[i] = sum / (float64(len(values)) * bandwidth * math.Sqrt(2*math.Pi))
	}
	return grid, density
}

func finitePointRect(x, y []float64) geom.Rect {
	var rect geom.Rect
	have := false
	for i := 0; i < min(len(x), len(y)); i++ {
		if !isFinite(x[i]) || !isFinite(y[i]) {
			continue
		}
		pt := geom.Pt{X: x[i], Y: y[i]}
		if !have {
			rect = geom.Rect{Min: pt, Max: pt}
			have = true
			continue
		}
		rect = expandRect(rect, pt)
	}
	if !have {
		return geom.Rect{Min: geom.Pt{}, Max: geom.Pt{X: 1, Y: 1}}
	}
	return rect
}

func piePoint(center geom.Pt, radius, angleDeg float64) geom.Pt {
	rad := angleDeg * math.Pi / 180
	return geom.Pt{
		X: center.X + radius*math.Cos(rad),
		Y: center.Y + radius*math.Sin(rad),
	}
}

func pieAlign(angleDeg float64) TextAlign {
	rad := angleDeg * math.Pi / 180
	switch {
	case math.Cos(rad) < -0.2:
		return TextAlignRight
	case math.Cos(rad) > 0.2:
		return TextAlignLeft
	default:
		return TextAlignCenter
	}
}

func pieLabelOffset(angleDeg float64) float64 {
	rad := angleDeg * math.Pi / 180
	switch {
	case math.Cos(rad) < -0.2:
		return -6
	case math.Cos(rad) > 0.2:
		return 6
	default:
		return 0
	}
}

func sankeyOrientation(flow float64, orientations []int, i int) int {
	if i < len(orientations) {
		switch {
		case orientations[i] < 0:
			return -1
		case orientations[i] > 0:
			return 1
		}
	}
	if flow < 0 {
		return -1
	}
	return 1
}

func floatAt(values []float64, i int, fallback float64) float64 {
	if i < len(values) && isFinite(values[i]) {
		return values[i]
	}
	return fallback
}

func boolOffset(v bool) int {
	if v {
		return 1
	}
	return 0
}

func specialtyBool(value *bool, fallback bool) bool {
	if value == nil {
		return fallback
	}
	return *value
}
